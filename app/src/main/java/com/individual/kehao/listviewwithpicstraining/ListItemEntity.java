public class ListItemEntity {
    private String userName;
    private String time;
    private String skoop;
    private String userPicUrl;
    private String skoopPicUrl;

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getSkoop() {
        return skoop;
    }

    public void setSkoop(String skoop) {
        this.skoop = skoop;
    }

    public String getUserPicUrl() {
        return userPicUrl;
    }

    public void setUserPicUrl(String userPicUrl) {
        this.userPicUrl = userPicUrl;
    }

    public String getSkoopPicUrl() {
        return skoopPicUrl;
    }

    public void setSkoopPicUrl(String skoopPicUrl) {
        this.skoopPicUrl = skoopPicUrl;
    }
}
